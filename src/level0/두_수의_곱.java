public class 두_수의_곱 {
    public static void main(String[] args) {
        System.out.println(solution(3, 4));     // 12
        System.out.println(solution(27, 19));   // 513
    }

    public static int solution(int num1, int num2) {
        return num1 * num2;
    }
}
